package sec06.exam06;

public class Car2 {
	
	private int speed;
	private boolean stop;
	
	// getter, setter 자동 완성
	// 마우스 오른쪽 버튼 -> source -> Generate Getters and Setters
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public boolean isStop() {
		return stop;
	}
	public void setStop(boolean stop) {
		this.stop = stop;
	}
	
	
	
}
